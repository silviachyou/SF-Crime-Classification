var sanFrancisco = new google.maps.LatLng(37.774546, -122.433523);

function initMap() {
	var mapOptions = {
		center: sanFrancisco,
		zoom: 12,
		styles: mapstyle
	};
	map = new google.maps.Map(
		document.getElementById('map-canvas'), mapOptions
	);

}

google.maps.event.addDomListener(window, 'load', initMap);
